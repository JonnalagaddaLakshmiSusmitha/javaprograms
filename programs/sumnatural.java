import java.util.*;
  class sumnatural
    {
      public static void main(String args[])
      {
        Scanner sc=new Scanner(System.in);
        System.out.println("enter a number");
        int n=sc.nextInt();
        int sum=0;
        for(int i=1;i<1;i++)
          {
            System.out.println(i);
            sum=sum+i;
          }
        System.out.println("the sum of"+n+"natural number is"+sum);
      }
    }